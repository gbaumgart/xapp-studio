package pmedia.types;

public class Rating 
{
	private String context;
	private float pscoreTotal;
	private int pscoreCount;
	private float pscore;
	private float mscoreTotal;
	private int mscoreCount;
	private int contextId;
	/**
	 * @return the context
	 */
	public String getContext() {
		return context;
	}
	/**
	 * @param context the context to set
	 */
	public void setContext(String context) {
		this.context = context;
	}
	/**
	 * @return the pscoreTotal
	 */
	public float getPscoreTotal() {
		return pscoreTotal;
	}
	/**
	 * @param pscoreTotal the pscoreTotal to set
	 */
	public void setPscoreTotal(float pscoreTotal) {
		this.pscoreTotal = pscoreTotal;
	}
	/**
	 * @return the pscoreCount
	 */
	public int getPscoreCount() {
		return pscoreCount;
	}
	/**
	 * @param pscoreCount the pscoreCount to set
	 */
	public void setPscoreCount(int pscoreCount) {
		this.pscoreCount = pscoreCount;
	}
	/**
	 * @return the pscore
	 */
	public float getPscore() {
		return pscore;
	}
	/**
	 * @param pscore the pscore to set
	 */
	public void setPscore(float pscore) {
		this.pscore = pscore;
	}
	/**
	 * @return the mscoreTotal
	 */
	public float getMscoreTotal() {
		return mscoreTotal;
	}
	/**
	 * @param mscoreTotal the mscoreTotal to set
	 */
	public void setMscoreTotal(float mscoreTotal) {
		this.mscoreTotal = mscoreTotal;
	}
	/**
	 * @return the mscoreCount
	 */
	public int getMscoreCount() {
		return mscoreCount;
	}
	/**
	 * @param mscoreCount the mscoreCount to set
	 */
	public void setMscoreCount(int mscoreCount) {
		this.mscoreCount = mscoreCount;
	}
	/**
	 * @return the contextId
	 */
	public int getContextId() {
		return contextId;
	}
	/**
	 * @param contextId the contextId to set
	 */
	public void setContextId(int contextId) {
		this.contextId = contextId;
	}
	
	
		
}
